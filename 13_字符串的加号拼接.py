"""
字符串的拼接
"""

print("大模型","66666")

# 在python中，+号用于数字是数学计算，用于字符串就是2个字符串拼接在一起
print("大模型"+"666")


name = "周杰伦"
age = 11
height = 172.55
# 组装信息到info变量中，信息格式是： 我是XXX，今年XXX岁，身高XXX厘米
info =  "我是"+name + ", 今年"+str(age)+"岁, " + "身高"+str(height)+"厘米"
print(info)

# 字符串乘以数字，可以做到将字符串复制多少份前后拼接到一起
print("-"*50)