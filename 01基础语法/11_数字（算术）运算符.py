"""
运算符的使用
"""

print("5+2=",5+2)            # 应用加法
print("5-2=",5-2)            # 应用减法
print("5*2=",5*2)            # 应用乘法
print("5/2=",5/2)            # 应用除法， 非整除
print("5//2=",5//2)          # 应用整除
print("5%2=",5%2)            # 应用取余
print("5 ** 2=",5 ** 2)      # 应用指数计算



# 复合运算符 基础架构： ?=
