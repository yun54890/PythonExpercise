"""
语法；
{元素,元素,...,...,元素}
元素必须是： key:value  称之为键值对 kV对
key 可以是任意类型（不可以是数据容器）
value 可以是任意类型（可以是数据容器）
无法用下标，可以用key找到对应的value
就像生活中查字典，用字找到对应的含义
"""


# 空字典
d = {}
print(type(d))
d = dict()
print(type(d))

# 字面量
{"周杰伦":99,"王力宏":88,"林俊杰":77}

#  变量
d = {"周杰伦":99,"王力宏":88,"林俊杰":77,0:66}
print(d)

# 没有下标 d[0]  本质上是找0这个key  而不是找0这个下标
# 访问元素的语法: 字典变量[key]   注意[]里面不是下标而是key的值
print(d[0])
print(d["周杰伦"])

# 字典的key是不可以重复的
d = {"周杰伦":99,"王力宏":88,"林俊杰":77,0:66,"林俊杰":99}
# 打印出来之后  第二个林俊杰覆盖第一个林俊杰
print(d)



